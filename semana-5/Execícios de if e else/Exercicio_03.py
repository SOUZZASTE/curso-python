# Solicitar o tipo sanguineo

# Caso seja do tipo A o O

# A pessoa é um possivel doador

tipoSanguineo = str(input("Informe o se tipo sanguineo: "))
if tipoSanguineo.upper() == "A" or tipoSanguineo.upper() == "O":
    print("Você é um possivel doador")

print("Tipo sanguineo informado: " + tipoSanguineo)
