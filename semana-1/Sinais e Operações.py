#Sinais de Comparação

#Igual a (Indica que os os dois número são iguais)
print (4==4) # >False -> 4 não é igual a 5
print (4==5) # >True -> 4 é igual 4

# Diferente de ( Indica se dois valores não são iguais)
print(6!=3) # >Treu -> 6 não é igual a 3
print(6!=6) # >False -> 6 é igual a 6

# Maior que (Indica se o número anterior ao sinal é maior que o porterior)
print(9>3) # >True -> 9 é maio que 3
print(3>5) # >False -> 3 não é maior do que 5

#Maior ou igual a (Indica se o número anterior ao sinal é maior ou igual ao número posterior)
print(10>=10) # >True -> 10 é igual a 10
print (13>=140) # >False -> 13 não é maior e não é igual a 140

# Menor (Indica se o número anteior ao sinal é menor que o número posteior)
print (63<65) # >True -> 63 é menor que 65
print (150<140) # >False -> 150 não é menor que 150

# Menor ou igual a (Indica se o número anterior ao sinal é menor ou igual ao número posterior)
print(5<=5) # <True -> 5 é igual a 5
print(12<=8) # <False -> 12 não é menor ou igual a 8